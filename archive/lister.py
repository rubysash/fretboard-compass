# Split progressions and chords into lists
progressions = progressions_full.split('\n')
progressions_split = [progression.split() for progression in progressions]
chords = chords_full.split('\n')

# Create a set to store unique chords found in progressions
unique_progression_chords = set()

# Iterate over progressions to collect unique chords
for progression in progressions_split:
    for chord in progression:
        unique_progression_chords.add(chord)

# Convert the list of chords to a set for efficient comparison
all_chords_set = set(chords)

# Find missing chords
missing_chords = unique_progression_chords - all_chords_set

# Print missing chords
if missing_chords:
    print("Missing chords:")
    for chord in missing_chords:
        print(chord)
else:
    print("No missing chords.")


